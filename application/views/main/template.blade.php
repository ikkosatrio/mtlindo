            },
            function(){
            if(data.auth!=false){
                // redirect("{{base_url('main/contact')}}");
                $('#form-pesan').trigger("reset");
            return;
            }}
            );

            })
            .fail(function() {
            $('html').unblock();
            sweetAlert({
            title:  "Opss!",
            text:   "Something Wrong!, Try Again later",
            type:   "error",
